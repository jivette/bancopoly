package juego;

/**
 *
 * @author OliverAmitahi
 */
public class Jugador {
    
    
    int pos = 0;
    double saldo = 500;
    int propiedad = 0;
    int descanzo = 0;


    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }

    public int getPropiedad() {
        return propiedad;
    }

    public void setPropiedad(int propiedad) {
        this.propiedad = propiedad;
    }

    public int getDescanzo() {
        return descanzo;
    }

    public void setDescanzo(int descanzo) {
        this.descanzo = descanzo;
    }
            
     
    
    
}
